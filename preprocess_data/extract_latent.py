class DreamBoothDataset(Dataset):
    """
    A dataset to prepare the instance and class images with the prompts for fine-tuning the model.
    It pre-processes the images and the tokenizes prompts.
    """

    def __init__(
        self,
        instance_data_root,
    ):

        self.instance_data_root = Path(instance_data_root)
        if not self.instance_data_root.exists():
            raise ValueError(f"Instance {self.instance_data_root} images root doesn't exists.")

        # self.instance_images_path = list(Path(instance_data_root).iterdir())
        self.instance_images_path = []
        with open(self.instance_data_root, 'r') as file:
            for line in file:
                self.instance_images_path.append(line.strip().split(",")[0:2])  # strip() removes any leading/trailing whitespaces

        self.num_instance_images = len(self.instance_images_path)
        self._length = self.num_instance_images

        self.image_transforms = transforms.Compose(
            [
                transforms.ToTensor(),
                transforms.Normalize([0.5], [0.5]),
            ]
        )
       
        self.mask_transforms = transforms.Compose(
            [
                transforms.ToTensor(),
            ]
        )

    def __len__(self):
        return self._length

    def __getitem__(self, index):
        example = {}
        image_path, mask_path = self.instance_images_path[index % self.num_instance_images]
        example["image_path"] = image_path
        example["mask_path"] = mask_path

        try:
            instance_image = Image.open(image_path)
        except:
            return self.__getitem__(0)

        if mask_path != "None":
            mask_image = (np.array(Image.open(mask_path))[: , :, :3] / 255.0).astype(np.uint8)
            instance_image_arr = np.array(instance_image)[: , :, :3]
            instance_image_arr = instance_image_arr * mask_image
            instance_image = Image.fromarray(instance_image_arr)

        if not instance_image.mode == "RGB":
            instance_image = instance_image.convert("RGB")
        
        instance_image = instance_image.resize((640, 480), Image.BILINEAR)
        example["instance_images"] = self.image_transforms(instance_image) # 480 640
        
        return example



def model_has_vae(args):
    config_file_name = os.path.join("vae", AutoencoderKL.config_name)
    if os.path.isdir(args.pretrained_model_name_or_path):
        config_file_name = os.path.join(args.pretrained_model_name_or_path, config_file_name)
        return os.path.isfile(config_file_name)
    else:
        files_in_repo = model_info(args.pretrained_model_name_or_path, revision=args.revision).siblings
        return any(file.rfilename == config_file_name for file in files_in_repo)


def main(args):
    logging_dir = Path(args.output_dir, args.logging_dir)

    accelerator_project_config = ProjectConfiguration(project_dir=args.output_dir, logging_dir=logging_dir)

    accelerator = Accelerator(
        gradient_accumulation_steps=args.gradient_accumulation_steps,
        mixed_precision=args.mixed_precision,
        log_with=args.report_to,
        project_config=accelerator_project_config,
    )

  
    # import correct text encoder class
    if model_has_vae(args):
        vae = AutoencoderKL.from_pretrained(
            args.pretrained_model_name_or_path, subfolder="vae", revision=args.revision
        )
    else:
        vae = None

    if vae is not None:
        vae.requires_grad_(False)

    # Dataset and DataLoaders creation:
    train_dataset = DreamBoothDataset(
        instance_data_root=args.instance_data_dir,
    )
    train_dataset[0] # for debugging

    train_dataloader = torch.utils.data.DataLoader(
        train_dataset,
        batch_size=args.train_batch_size,
        shuffle=False,
        num_workers=args.dataloader_num_workers,
        persistent_workers=True,
    )

    weight_dtype = torch.float32
    if vae is not None:
        vae.to(accelerator.device, dtype=weight_dtype)

    train_dataloader = accelerator.prepare(train_dataloader)

    for step, batch in tqdm(enumerate(train_dataloader)):
        model_input = vae.encode(batch["instance_images"].to(dtype=weight_dtype)).latent_dist.sample()

        for image_path, precompute_latent in zip(batch["image_path"], model_input):
            save_path = image_path.replace("data", "data_latent")
            save_path = save_path + ".npy"
            
            image_dir = "/".join(save_path.split("/")[:-1])
            os.makedirs(image_dir, exist_ok=True)
            np.save(save_path, precompute_latent.cpu().numpy())
                

if __name__ == "__main__":
    args = parse_args()
    main(args)